function renderDrawer(content: PlannerContentDetail) {
  const queryClient = new QueryClient({
    defaultOptions: {
      queries: { retry: false },
    },
  });
  const invalidateSpy = vi.spyOn(queryClient, "invalidateQueries");

  vi.stubGlobal(
    "fetch",
    vi.fn().mockResolvedValue({
      ok: true,
      json: async () => content,
    }),
  );

  render(
    <QueryClientProvider client={queryClient}>
      <PostDrawer contentId="content-1" open onOpenChange={vi.fn()} />
    </QueryClientProvider>,
  );

  return { invalidateSpy };
}

describe("<PostDrawer /> media editing", () => {
  afterEach(() => {
    cleanup();
    vi.unstubAllGlobals();
    vi.clearAllMocks();
  });

  it("shows an edit button for media on editable posts", async () => {
    renderDrawer(makeContent());

    expect(await screen.findByText("Media (1)")).toBeInTheDocument();
    expect(screen.getByRole("button", { name: /edit media/i })).toBeInTheDocument();
  });

  it("refreshes the drawer query and planner after media is saved", async () => {
    const { invalidateSpy } = renderDrawer(makeContent());

    await screen.findByText("Media (1)");
    fireEvent.click(screen.getByRole("button", { name: /edit media/i }));

    await waitFor(() => {
      expect(invalidateSpy).toHaveBeenCalledWith({ queryKey: ["content-detail", "content-1"] });
    });
    expect(routerRefresh).toHaveBeenCalled();
  });
});
